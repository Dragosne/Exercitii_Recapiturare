# variabila integer
i = 100
print('Tipul de variabile: ', i, '- este de tipul: ', type(i))

# variabila string
s = 'caractere'
print('Tipul de variabile: ', s, '- este de tipul: ', type(s))

# variabila boolean
b = True
print('Tipul de variabile: ', b, '- este de tipul: ', type(b))

# variabila float
f = 20.345
print('Tipul de variabile: ', f, '- este de tipul: ', type(f))

# variabila complex
c = 10+3j
print('Tipul de variabile: ', c, '- este de tipul: ', type(c))

# List - Tuple
list = ['tricou','pantalon', 'sapca']
print('lista: ', list, 'este de tipul: ', type(list))

tuple = ('tricou','pantalon', 'sapca')
print('tuple: ', tuple, 'este de tipul: ', type(tuple))

# Dict - Dictionary
NumeVarsta = {'Gheorghe' : 23, 'Popy' : 33, 'Sandu': 45}
print('dict', NumeVarsta, 'este de tipul ', type(NumeVarsta))

